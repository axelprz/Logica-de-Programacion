
package domain;

public class Operaciones {
    public void sumar(int a, int b){
        System.out.println(a + " + " + b + " = " + (a + b));
    }
    
    public void restar(int a, int b){
        System.out.println(a + " - " + b + " = " + (a - b));
    }
    
    public void multiplicar(int a, int b){
        System.out.println(a + " * " + b + " = " + (a * b));
    }
    
    public void dividir(double a, double b){
        System.out.println(a + " / " + b + " = " + (a / b));
    }
}
